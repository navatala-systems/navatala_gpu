#include <cuda_runtime.h>
extern "C" __global__ void navatala_dataframe_split_copy_i32(const int* src, const unsigned int* srcOffset, const unsigned int* count, int* dst) {
  int gid0 = (int)(blockIdx.x * blockDim.x + threadIdx.x);
  unsigned int gid = ((unsigned int)((int)(blockIdx.x * blockDim.x + threadIdx.x)));
  unsigned int n = count[0u];
  unsigned int offset = srcOffset[0u];
  bool inBounds = (gid < n);
  if (inBounds) {
    unsigned int srcIdx = (offset + gid);
    int value = src[srcIdx];
    dst[gid] = value;
  }
}
